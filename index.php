        <p class="my-6 text-justify">
            Dalam perjalanan sejarah industri olahraga, apa yang menjadi pembeda antara kultur olahraga di negara-negara
            yang sudah mapan seperti Eropa dan Amerika Serikat dengan negara-negara Asia atau Afrika, misalnya. Mari kita lihat
            kultur olahraga di negara-negara maju seperti Amerika Serikat dan negara-negara berkembang seperti Indonesia,
            India, dan Afrika. Di negara berkembang, tempat olahraga (terlebih untuk sebuah kompetisi besar), fasilitas-fasilitas
            masih terlihat sangat kurang apabila dibandingkan dengan fasilitas di negara maju, seperti fasilitas kesehatan yang masih
            kurang cepat dan kurang SDM dalam menangani suatu kasus kesehatan, naturalisasi dari mindset orang-orang yang tinggal di negara maju
            dan negara berkembang sudah sangat jauh berbeda, dan lain sebagainya.

            Salah satu kasus terburuk dalam sejarah sepak bola adalah Tragedi Kanjuruhan,
            Malang, Indonesia, 1 Oktober lalu. Ratusan orang meninggal akibat gas air mata yang dilontarkan polisi karena kurangnya
            pengetahuan, pengelolaan, dan pengendalian massa. Hal ini juga diperparah dengan fasilitas stadion yang kurang
            memadai dan minimnya pengetahuan mitigasi bencana. Berikut adalah penjelasan mengenai komparasi antara zona
            mitigasi dan peraturan mengenai penembakan gas air mata, sehingga mengakibatkan massa di stadion Kanjuruhan
            kemarin tidak bisa keluar dan harus merusak pintu stadion secara paksa.
        </p>

